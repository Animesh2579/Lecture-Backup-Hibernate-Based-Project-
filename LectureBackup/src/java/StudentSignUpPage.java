    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String enrId=jTextField1.getText();
        String name=jTextField4.getText();
        String contactNo=jTextField3.getText();
        String gender = null;
        if(jRadioButton1.isSelected())
            gender="Male";
        if(jRadioButton2.isSelected())
            gender="Female";
        String emailId=jTextField2.getText();
        char ch[]=jPasswordField3.getPassword();
        String pass=new String(ch);
        StudentSignUp ssu=new StudentSignUp(enrId,name,contactNo,emailId,gender,pass);
        session.save(ssu);
        tx.commit();
        session.close();
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        buttonGroup1.clearSelection();
        jPasswordField3.setText("");
        JOptionPane.showMessageDialog(this,"You are now registered...");
        new Login().setVisible(true);
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

